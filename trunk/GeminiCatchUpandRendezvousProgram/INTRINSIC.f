      FUNCTION ABSF (X)
      ABSF = ABS(X)
      RETURN
      END
C
      FUNCTION SIGNF (X,Y)
      SIGNF = SIGN(X,Y)
      RETURN
      END
C
      REAL FUNCTION INTF (X)
      INTF = AINT(X)
      RETURN
      END
C
      FUNCTION SQRTF (X)
      SQRTF = SQRT (X)
      RETURN
      END
C
      FUNCTION SINF (X)
      SINF = SIN (X)
      RETURN
      END
C
      FUNCTION COSF (X)
      COSF = COS (X)
      RETURN
      END
C
      FUNCTION ARCOSF (X)
      ARCOSF = ACOS (X)
      RETURN
      END
C
      FUNCTION ARSINF (X)
      ARSINF = ASIN (X)
      RETURN
      END
C
