#include "srcmap.h"

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#define SRCMAP_STMT_FIELD_WIDTH 4
#define SRCMAP_TEXT_WIDTH 101
/* 0-based indices into a report line, per pass1.rpt's confirmed fixed
 * layout: "NNNN X|<101 chars>|<revision field>". */
#define SRCMAP_COL1_INDEX 5
#define SRCMAP_BAR1_INDEX 6
#define SRCMAP_TEXT_START 7
#define SRCMAP_BAR2_INDEX (SRCMAP_TEXT_START + SRCMAP_TEXT_WIDTH)
#define SRCMAP_MIN_LEN (SRCMAP_BAR2_INDEX + 1)

static char *read_whole_file(const char *path, size_t *out_size) {
    FILE *f = fopen(path, "rb");
    if (!f) return NULL;
    if (fseek(f, 0, SEEK_END) != 0) { fclose(f); return NULL; }
    long size = ftell(f);
    if (size < 0 || fseek(f, 0, SEEK_SET) != 0) { fclose(f); return NULL; }
    char *buf = malloc((size_t)size + 1);
    if (!buf) { fclose(f); return NULL; }
    size_t nread = fread(buf, 1, (size_t)size, f);
    fclose(f);
    buf[nread] = '\0';
    *out_size = nread;
    return buf;
}

static bool parse_stmt_field(const char *line, long *out) {
    char field[SRCMAP_STMT_FIELD_WIDTH + 1];
    memcpy(field, line, SRCMAP_STMT_FIELD_WIDTH);
    field[SRCMAP_STMT_FIELD_WIDTH] = '\0';

    bool has_digit = false;
    for (int i = 0; i < SRCMAP_STMT_FIELD_WIDTH; i++) {
        char c = field[i];
        if (c >= '0' && c <= '9') { has_digit = true; continue; }
        if (c == ' ') continue;
        return false; /* not a numeric statement field (e.g. blank, or garbage) */
    }
    if (!has_digit) return false;
    *out = strtol(field, NULL, 10);
    return true;
}

bool halmat_srcmap_load(const char *path, halmat_srcmap_t *out, char *errbuf, size_t errbuf_size) {
    memset(out, 0, sizeof(*out));

    size_t size = 0;
    char *buf = read_whole_file(path, &size);
    if (!buf) {
        snprintf(errbuf, errbuf_size, "cannot read PASS1 report '%s'", path);
        return false;
    }

    size_t capacity = 256;
    halmat_srcmap_line_t *lines = malloc(capacity * sizeof(halmat_srcmap_line_t));
    if (!lines) {
        snprintf(errbuf, errbuf_size, "out of memory parsing '%s'", path);
        free(buf);
        return false;
    }
    size_t count = 0;

    char *line_start = buf;
    while (line_start < buf + size) {
        char *nl = strchr(line_start, '\n');
        size_t line_len = nl ? (size_t)(nl - line_start) : strlen(line_start);
        /* Report lines are CR-free in practice, but strip a trailing \r
         * defensively (pass1.rpt is generated by a DOS-heritage toolchain
         * per this project's other companion files). */
        size_t trimmed_len = line_len;
        if (trimmed_len > 0 && line_start[trimmed_len - 1] == '\r') trimmed_len--;

        if (trimmed_len >= SRCMAP_MIN_LEN &&
            line_start[SRCMAP_BAR1_INDEX] == '|' &&
            line_start[SRCMAP_BAR2_INDEX] == '|') {
            long stmt;
            if (parse_stmt_field(line_start, &stmt)) {
                char col1 = line_start[SRCMAP_COL1_INDEX];
                if (count >= capacity) {
                    capacity *= 2;
                    halmat_srcmap_line_t *grown = realloc(lines, capacity * sizeof(halmat_srcmap_line_t));
                    if (!grown) {
                        snprintf(errbuf, errbuf_size, "out of memory parsing '%s'", path);
                        for (size_t i = 0; i < count; i++) free(lines[i].text);
                        free(lines);
                        free(buf);
                        return false;
                    }
                    lines = grown;
                }
                char *text = malloc(SRCMAP_TEXT_WIDTH + 2);
                text[0] = (col1 == 'M') ? ' ' : col1;
                memcpy(text + 1, line_start + SRCMAP_TEXT_START, SRCMAP_TEXT_WIDTH);
                text[1 + SRCMAP_TEXT_WIDTH] = '\0';
                size_t tlen = 1 + SRCMAP_TEXT_WIDTH;
                while (tlen > 0 && text[tlen - 1] == ' ') text[--tlen] = '\0';

                lines[count].stmt = stmt;
                lines[count].text = text;
                count++;
            }
        }

        if (!nl) break;
        line_start = nl + 1;
    }

    free(buf);
    out->lines = lines;
    out->count = count;
    return true;
}

void halmat_srcmap_free(halmat_srcmap_t *map) {
    for (size_t i = 0; i < map->count; i++) free(map->lines[i].text);
    free(map->lines);
    map->lines = NULL;
    map->count = 0;
}

const halmat_srcmap_line_t *halmat_srcmap_find(const halmat_srcmap_t *map, long stmt, size_t *out_count) {
    for (size_t i = 0; i < map->count; i++) {
        if (map->lines[i].stmt != stmt) continue;
        size_t n = 1;
        while (i + n < map->count && map->lines[i + n].stmt == stmt) n++;
        *out_count = n;
        return &map->lines[i];
    }
    return NULL;
}
