C
C MRFBOTH: COMPARE HALSTAT MRF AND FAD UNRESOLVED REFS TABLE.  SHOW THE
C   OUTPUT WRITER SOURCE FOR EACH.
C
      PROGRAM MRFBOTH
      INCLUDE (COMMBLK)
      PARAMETER(ISDFSELECT=4,IPTRLOCATE=5,IDIRLOCATE=7,
     X  IBLKLOCATENUM=8,ISYMBLOCATENUM=9,ISTMTLOCATENUM=10,
     X  IBLKLOCATENAME=11,ISYMBLOCATEBLKNAME=12,
     X  ISYMBLOCATENAME=13,ISTMTLOCATESRN=14,MAXREAS=200,
     X  IBLOCKNDXLOCATENUM=15,ISYMBNDXLOCATENUM=16,
     X  ISTMTNDXLOCATENUM=17,MAXMEMBS=300)
      PARAMETER (MAXQUOTES=200,MAXEQUATES=200,MAXMATCHES=10000,
     X  MAXTERMS=2000,MAXSTRUCS=500)
      CHARACTER DLINE*255,CTEMP*32,CSRN*6,CNEXTSRN*6,CHAR1*1,
     X  CSTRUCTYPE*1,CALTREF*1,CAUSE*48,CSTMTTYPE*24,CNAME*4,
     X  CTEMPX*32,CPREVSOURCE*32,FILENAME1*32,FILENAME2*32,
     X  CSOURCENAME1*32,CSOURCENAME2*32,CSOURCE21*8,CSOURCE22*8,
     X  CSRN1*6,CSRN2*6
      CHARACTER*255 GETSTRING,TEMPLINE,ELINE,MAKESRC,ELINE1,ELINE2,
     X  DLINE1,DLINE2
      CHARACTER*128 CFIELD(MAXQUOTES)
      CHARACTER*16 CFNAME(MAXQUOTES)
      INTEGER IQUOTELOC(MAXQUOTES)
      COMMON /CQ1/ CFIELD,CFNAME,IQUOTELOC
      INTEGER IEQUSYMB1(MAXEQUATES),IEQUSYMB2(MAXEQUATES)
      CHARACTER CSRNEQU*6(MAXEQUATES),CSYMBNAMEQU*32(MAXEQUATES)
      INTEGER INCLCNTEQU(MAXEQUATES),IEQUSTMT(MAXEQUATES)
      INTEGER ISYMBMATCH(MAXMATCHES)
C
C THE "TERMID" ARRAY WILL CONTAIN THE SDF SYMBOL NUMBERS OF ALL
C   PARTS OF A STRUCTURE TEMPLATE, INCLUDING THE TEMPLATE ITSELF,
C   THE MINOR STRUCTURES, AND THE TERMINALS.  THE "TERMIDTPL" ARRAY
C   WILL CONTAIN THE SDF SYMBOL NUMBER OF THE STRUCTURE TEMPLATE.
C   THIS WILL ELIMINATE THE NEED FOR STRUCTURE WALKS WHEN THE
C   TEMPLATE ITSELF IS OF INTEREST.
C THE "TPLID" ARRAY WILL CONTAIN THE SDF SYMBOL NUMBER OF ALL
C   STRUCTURE TEMPLATES.  THE "TPLIDSTRUC" ARRAY WILL CONTAIN THE
C   SDF SYMBOL NUMBER OF A STRUCTURE THAT USES THAT SPECIFIC TEMPLATE.
C   THUS THE SAME "TPLID" ENTRY MAY OCCUR SEVERAL TIMES IF THE
C   TEMPLATE IS USED FOR MORE THAN ONE STRUCTURE.
C
      INTEGER TERMID(MAXTERMS),TERMIDTPL(MAXTERMS)
      INTEGER STRUCID(MAXSTRUCS),TPLID(MAXSTRUCS)
      COMMON /CT1/ TERMID,TERMIDTPL,STRUCID,TPLID
C
C DATA SPECIFICALLY FOR MASTER LISTING
C
      CHARACTER CMEMBNAME*8(MAXMEMBS)
      CHARACTER CREASONS*128(MAXREAS)
C
C READ THE MRF FILE.
C
      CLOSE(IG1)
      DO II = 1,MAXREAS
        CREASONS(II) = ' '
      ENDDO
      FILENAME1 = '/DSSPCS.FADMRF2'
      FILENAME2 = '/DSSPCS.HALMRF2'
      OPEN(IG1,FILE=FILENAME1,STATUS='OLD',
     X  IOSTAT=IOPEN,FORM='UNFORMATTED',ACTION='READ',
     X  ACCESS='SEQUENTIAL')
      IF (IOPEN .NE. 0) THEN
        WRITE(IP,*) '*** INPUT1 OPEN ERROR; ',IOPEN
        STOP 4000
      ENDIF
      OPEN(IG2,FILE=FILENAME2,STATUS='OLD',
     X  IOSTAT=IOPEN,FORM='UNFORMATTED',ACTION='READ',
     X  ACCESS='SEQUENTIAL')
      IF (IOPEN .NE. 0) THEN
        WRITE(IP,*) '*** INPUT2 OPEN ERROR; ',IOPEN
        STOP 4000
      ENDIF
      ILINENUM = 0
      CPREVSOURCE = ' '
C
C READ THE FIRST LINE OF EACH FILE
C
      IREAD1 = 1
      IREAD2 = 1
      IEOF1 = 0
      IEOF2 = 0
 801  CONTINUE
      IF ((IREAD1 .EQ. 1).AND.(IEOF1 .EQ. 0)) THEN
        ELINE1 = ' '
        READ(IG1,ERR=8011,END=8011) ELINE1(1:133)
        IF (.FALSE.) THEN
 8011     CONTINUE
          IEOF1 = 1
          ELINE1 = ' '
          IREAD1 = 0
          DLINE1 = ' '
          GO TO 803
        ENDIF
        IX = LENTRIM(ELINE1)
        IF (IX .LE. 1) IX = 2
        DLINE1 = ELINE1(2:IX)
        IX = LENTRIM(DLINE1)
        IF (IX .EQ. 0) IX = 1
        IF (DLINE1 .EQ. ' ') GO TO 801
C PARSE THE FAD LINE
        NUMQUOTES = 0
        DLINE = DLINE1
        DO II = 1,IX
          IF (DLINE(II:II) .EQ. '"') DLINE(II:II) = ' '
        ENDDO
        DO II = 1,IX
          IF (DLINE(II:II) .EQ. ',') THEN
            NUMQUOTES = NUMQUOTES + 1
            IF (NUMQUOTES .GT. MAXQUOTES) THEN
              WRITE(IP,*)
     X          '*** MAXQUOTES EXCEEDED: ',MAXQUOTES
            ENDIF
            IQUOTELOC(NUMQUOTES) = II
          ENDIF
        ENDDO
        IF (NUMQUOTES .EQ. 0) GO TO 801
C
C BLANK OUT OUR FIELDS ... AND THEN FILL THEM IN FROM THE STRING
C
        DO II = 1,MAXQUOTES
          CFIELD(II) = ' '
        ENDDO
        NUMFIELDS = NUMQUOTES + 1
        DO II = 1,NUMFIELDS
          IF (II .LE. NUMQUOTES) THEN
            IRIGHT = IQUOTELOC(II) - 1
          ELSE
            IRIGHT = IX
          ENDIF
          IF (II .GT. 1) THEN
            ILEFT = IQUOTELOC(II-1) + 1
          ELSE
            ILEFT = 1
          ENDIF
          IF (ILEFT .LE. IRIGHT) THEN
            CFIELD(II) = DLINE(ILEFT:IRIGHT)
          ENDIF
        ENDDO
        ELINE1 = CFIELD(1)
        CSOURCENAME1 = MAKESRC(ELINE1,6)
        CSOURCE21 = MAKESRC(ELINE1,8)
        CSRN1 = CFIELD(3)
        CALL MAKESRN(CSRN1)
        IX1 = LENTRIM(DLINE1)
C       WRITE(IP,*) ' FAD: ',DLINE1(1:IX)
C       WRITE(IP,*) 'CSOURCENAME1/CSRN1 = ',CSOURCENAME1,' ',
C    X    CSRN1
        IREAD1 = 0
      ENDIF
 803  CONTINUE
      IF ((IREAD2 .EQ. 1).AND.(IEOF2 .EQ. 0)) THEN
        ELINE2 = ' '
        READ(IG2,ERR=8031,END=8031) ELINE2(1:133)
        IF (.FALSE.) THEN
 8031     CONTINUE
          IEOF2 = 1
          ELINE2 = ' '
          DLINE2 = ' '
          IREAD2 = 0
          GO TO 804
        ENDIF
        IX = LENTRIM(ELINE2)
        IF (IX .LE. 1) IX = 2
        DLINE2 = ELINE2(2:IX)
        IX = LENTRIM(DLINE2)
        IF (IX .EQ. 0) IX = 1
        IF (DLINE2 .EQ. ' ') GO TO 801
        IF (DLINE2(1:1) .EQ. '|') GO TO 801
        IF (DLINE2(1:1) .EQ. '_') GO TO 801
        IF (DLINE2(1:7) .EQ. '  UNIT ') GO TO 801
        IF (DLINE2(20:20) .EQ. '.') THEN
          READ(DLINE2(18:19),9644,ERR=801) IREASNUM
9644      FORMAT(I2)
          CREASONS(IREASNUM) = DLINE2(23:IX)
          GO TO 801
        ENDIF
        ILINENUM = ILINENUM + 1
        IF (ILINENUM .LE. 6) GO TO 801
        CSRN2 = DLINE2(58:63)
        ELINE2 = DLINE2(26:50)
        IF (CSRN2 .EQ. ' ') GO TO 801
        IF (CSRN2(1:4) .EQ. 'REF=') GO TO 801
        IF (ELINE2 .EQ. ' ') GO TO 801
        CSOURCENAME2 = MAKESRC(ELINE2,6)
        CSOURCE22 = MAKESRC(ELINE2,8)
        CTEMP = DLINE2(51:54)
        CTEMPX = DLINE2(69:70)
        IX = LENTRIM(CTEMPX)
        IF (IX .EQ. 2) THEN
          READ(CTEMPX(1:2),9641) IREASNUM
 9641     FORMAT(I2)
        ELSE
          READ(CTEMPX(1:1),9642) IREASNUM
 9642     FORMAT(I1)
        ENDIF
        IF ((IREASNUM .EQ. 4).OR.(IREASNUM .EQ. 9).OR.
     X    (IREASNUM .EQ. 21).OR.(IREASNUM .EQ. 37)) GO TO 801
        IX2 = LENTRIM(DLINE2)
C       WRITE(IP,*) ' HAL: ',DLINE2(1:IX2)
C       WRITE(IP,*) 'CSOURCENAME2/CSRN2 = ',CSOURCENAME2,' ',
C    X    CSRN2
        IREAD2 = 0
      ENDIF
 804  CONTINUE
      IF ((IEOF1 .EQ. 1).AND.(IEOF2 .EQ. 1)) STOP
C
C WE NOW HAVE 2 MRF LINES, DLINE1 FROM FAD AND DLINE2 FROM HALSTAT
C
C PROCESS THE TWO STREAMS IN PARALLEL.
C
      IDO1 = 0
      IDO2 = 0
      IF ((IEOF1 .EQ. 0).AND.(IEOF2 .EQ. 1)) THEN
        IDO1 = 1
      ELSE IF ((IEOF1 .EQ. 1).AND.(IEOF2 .EQ. 0)) THEN
        IDO2 = 1
      ELSE IF ((IEOF1 .EQ. 0).AND.(IEOF2 .EQ. 0)) THEN
C       WRITE(IP,*) 'SOURCE1/2 = ',CSOURCE21,' ',CSOURCE22
C       WRITE(IP,*) 'SRN1/2 = ',CSRN1,' ',CSRN2
        IF (CSOURCE21 .LT. CSOURCE22) THEN
          IDO1 = 1
          IDO2 = 0
        ELSE IF (CSOURCE21 .EQ. CSOURCE22) THEN
          IF (CSRN1 .LT. CSRN2) THEN
            IDO1 = 1
            IDO2 = 0
          ELSE IF (CSRN1 .EQ. CSRN2) THEN
            IDO1 = 1
            IDO2 = 1
          ELSE IF (CSRN1 .GT. CSRN2) THEN
            IDO1 = 0
            IDO2 = 1
          ENDIF
        ELSE IF (CSOURCE21 .GT. CSOURCE22) THEN
          IDO1 = 0
          IDO2 = 1
        ENDIF
      ENDIF
      IF (IEOF1 .EQ. 1) IDO1 = 0
      IF (IEOF2 .EQ. 1) IDO2 = 0
      DO II = 1,80
        DLINE(II:II) = '*'
      ENDDO
      WRITE(IP,9000) DLINE(1:80)
      IF ((IDO1 .EQ. 1).AND.(IDO2 .EQ. 0)) THEN
        WRITE(IP,9621) 'FADONLY:',DLINE1(1:IX1)
 9621   FORMAT(1X,A8,2X,A)
        IREAD1 = 1
        IREAD2 = 0
        CSOURCENAME = CSOURCENAME1
        CSOURCE2 = CSOURCE21
        CSRN = CSRN1
      ELSE IF ((IDO1 .EQ. 1).AND.(IDO2 .EQ. 1)) THEN
        WRITE(IP,9621) 'FAD&HAL:',DLINE1(1:IX1)
        WRITE(IP,9621) 'FAD&HAL:',DLINE2(1:IX2)
        IREAD1 = 1
        IREAD2 = 1
        CSOURCENAME = CSOURCENAME1
        CSRN = CSRN1
        CSOURCE2 = CSOURCE21
      ELSE IF ((IDO1 .EQ. 0).AND.(IDO2 .EQ. 1)) THEN
        WRITE(IP,9621) 'HALONLY:',DLINE2(1:IX2)
        IREAD1 = 0
        IREAD2 = 1
        CSOURCENAME = CSOURCENAME2
        CSRN = CSRN2
        CSOURCE2 = CSOURCE22
      ELSE
        STOP
      ENDIF
C WE HAVE NOW ISOLATED A MRF LINE; EXTRACT THE CU NAME, ISN, SRN,
C   AND REASON CODE
C
      IF (IDO2 .EQ. 1) THEN
C       WRITE(IP,9317) CSOURCENAME,CTEMP,CSRN,CTEMPX
 9317   FORMAT(1X,A8,2X,A4,2X,A6,2X,A2)
        WRITE(IP,9456) IREASNUM,CREASONS(IREASNUM)
9456    FORMAT(' TYPE: ',I2,2X,A)
      ENDIF
      IF (CSOURCENAME .NE. CPREVSOURCE) THEN
        CALL READMLST
      ENDIF
      CPREVSOURCE = CSOURCENAME
      ASSIGN 561 TO ISUBRET
      GO TO 2400
 561  CONTINUE
C     ICNT = ICNT + 1
C     IF (ICNT .GT. 50) STOP
      GO TO 801
C     STOP
C     ICNT = ICNT + 1
C     IF (ICNT .GT. 20) STOP
      GO TO 801
 802  CONTINUE
C     DO II = 1,MAXREAS
C       IF (CREASONS(II) .NE. ' ') THEN
C         WRITE(IP,9311) II,CREASONS(II)
C9311     FORMAT(' REAS: ',I2,2X,A)
C       ENDIF
C     ENDDO
      STOP
 833  CONTINUE
      CSOURCENAME = CMEMBNAME(IMEMBNUM)
      WRITE(IP,*) '*** CSOURCENAME = ',CSOURCENAME
C
C READ THE COMPRESSED SOURCE CORRESPONDING TO THIS SDF
C
C     WRITE(IP,*) '*** CALLING READMLST'
      CALL READMLST
C     WRITE(IP,*) '*** AT F'
C
C SHOW OUR COMPRESSED LINES
C
C     WRITE(IP,*) '*** NUMRECS = ',NUMRECS
      IMLNUM = 0
 37   CONTINUE
      IMLNUM = IMLNUM + 1
      IF (IMLNUM .GT. NUMRECS) GO TO 39
C     WRITE(IP,*) '*** READING REC: ',IMLNUM
      READ(IGL,REC=IMLNUM) OUTBUF(1:133)
C
C PARSE THE LINE
C
      IF (OUTBUF(37:37) .EQ. '+') GO TO 37
      DLINE = OUTBUF(38:108)
      IF (DLINE .EQ. ' ') GO TO 37
      IF (DLINE(1:1) .EQ. '*') GO TO 37
      ISLABEL = 0
      IF (DLINE(1:1) .NE. ' ') ISLABEL = 1
C     WRITE(IP,*) '3CHARS:-',DLINE(1:1),'-',DLINE(2:2),
C    X  '-',DLINE(3:3)
      IX = LENTRIM(DLINE)
      WRITE(IP,9000) DLINE(1:IX)
      ASSIGN 38 TO ISUBRET
      GO TO 2500
 38   CONTINUE
C     WRITE(IP,*) '*** NUMFIELDS = ',NUMFIELDS
      IX = LENTRIM(DLINE)
      IF (IX .EQ. 0) IX = 1
C     WRITE(IP,9000) DLINE(1:IX)
C
C IF WE HAVE AN "ENTRY" KEYWORD, PRINT OUT THE FOLLOWING TOKEN
C
      IF (NUMFIELDS .GT. 0) THEN
        IMATCH = 0
        DO JJ = 1,NUMFIELDS
          IF (CFIELD(JJ) .EQ. 'ENTRY') THEN
            WRITE(IP,*) '*** FIELD MATCH: ',CFIELD(JJ+1)
            IMATCH = 1
          ENDIF
        ENDDO
C       IF (IMATCH .EQ. 1) WRITE(IP,9000) DLINE(1:IX)
      ENDIF
C     WRITE(IP,*) '***',IMLNUM,NUMRECS,NUMFIELDS
      GO TO 37
 39   CONTINUE
C     GO TO 33
 34   CONTINUE
      STOP
C
C DOCUMENT OUR FIELD NAMES:  THIS IS THE FORMAT OF THE REQUISITE INPUT FILE
C
      CFNAME(1) = 'SDF_NAME'
      CFNAME(2) = 'SRN'
      CFNAME(3) = 'INCLUDE_CNT'
      CFNAME(4) = 'BLOCK_NAME'
      CFNAME(5) = 'SYMBOL_NAME'
      CFNAME(6) = 'SUBSCRIPT_TEXT'
      CFNAME(7) = 'XREF_TYPE'
      CFNAME(8) = 'STRUC_XREF_TYPE'
      CFNAME(9) = 'INDIRECT_REF'
      CFNAME(10) = 'PARAMETER_FLAG'
      CFNAME(11) = 'AUTHORIZATION'
      CFNAME(12) = 'DATA_SOURCE'
C
C OPEN RELEVANT FILES
C
C     WRITE(IP,*) '*** ATTEMPTING TO OPEN SQL DATAFILE'
      OPEN(IG2,FILE='/DSSPCS.FORT.SQL.XREFS1',STATUS='OLD',
     X  IOSTAT=IOPEN,FORM='FORMATTED')
      IF (IOPEN .NE. 0) THEN
        WRITE(IP,*) '*** INPUT OPEN ERROR; ',IOPEN
        STOP 4000
      ENDIF
      OPEN(IG3,FILE='/DSSPCS.FORT.XREFS1.OUT',STATUS='UNKNOWN',
     X  IOSTAT=IOPEN,FORM='FORMATTED',ACTION='WRITE')
      IF (IOPEN .NE. 0) THEN
        WRITE(IP,*) '*** OUTPUT OPEN ERROR: ',IOPEN
        STOP 4602
      ENDIF
C
C INITIALIZE OUR GLOBAL VARIABLES
C   - BLANK OUT CSDFNAM TO SHOW NO SDF SELECTED
C
      CSDFNAM = ' '
      ITOTFADXREFS = 0
      IVALIDATED1 = 0
      IVALIDATED2 = 0
      ISKIPSTRUCNUM = 0
      IUNABLECNT = 0
C
C READ EACH LINE OF THE FAD DATA FILE
C
  5   CONTINUE
C     WRITE(IP,*) '*** AT 5'
      READ(IG2,9001,ERR=10,END=10) DLINE
 9001 FORMAT(A133)
      IX = LENTRIM(DLINE)
C     WRITE(IP,9000) DLINE(1:IX)
C     WRITE(IG3,9001) DLINE(1:133)
C
C SKIP EXTRANEOUS LINES
C
      IF (IX .EQ. 0) GO TO 5
      IF (DLINE(1:1) .NE. '#') GO TO 5
      ITOTFADXREFS = ITOTFADXREFS + 1
C     WRITE(IP,9000) DLINE(1:IX)
 9000 FORMAT(A)
C
C STORE THE SQL OUTPUT LINE IN OUR LINE BUFFER
C
      LINES(1) = DLINE
      NUML = 1
C
C SHOW THAT WE FOUND NO ERRORS IN THIS LINE
C
      IERROR = 0
C
C DETERMINE IF WE HAVE A REFERENCE TO A STRUCTURE ELEMENT:
C   "S=MAJOR STRUCT", "M=MINOR STRUCT". "T=TERMINAL"/
C
      CSTRUCTYPE = CFIELD(8)
C     IF ((CFIELD(8) .EQ. 'S').OR.(CFIELD(8) .EQ. 'T')) THEN
C       ISKIPSTRUCNUM = ISKIPSTRUCNUM + 1
C       GO TO 5
C     ENDIF
C
C READ THE APPROPRIATE SDF INTO MEMORY
C
      CTEMP = CFIELD(1)
C
C SKIP ANY TROUBLESOME SDFS
C
      ISKIP = 0
      IF (CTEMP .EQ. '##DCDDG9') ISKIP = 1
      IF (CTEMP .EQ. '##PGGPCF') ISKIP = 1
      IF (CTEMP .EQ. '##VAASEQ') ISKIP = 1
      IF (ISKIP .EQ. 1) THEN
        WRITE(IG3,9001) DLINE(1:133)
        GO TO 5
      ENDIF
      IF (CTEMP .EQ. CSDFNAM) GO TO 1000
C     WRITE(IP,*) '*** AT C'
C     icnt = icnt + 1
C     if (icnt .gt. 13) go to 10
C
C SELECT THE NEW SDF
C
      CSDFNAM = CTEMP
      WRITE(IP,*) '*** SELECTING SDF: ',CSDFNAM
C     WRITE(IP,*) '*** MODE = ',ISDFSELECT
      CALL SDFPKG(ISDFSELECT)
      IF (ICRETURN .NE. 0) THEN
        WRITE(IP,*) '*** SDF NOT FOUND: ',CSDFNAM
        STOP 4700
      ENDIF
C
C DUMP OUT THE COMPLETE SDF
C
C     DO IPAGE = 0,ILASTPAGE-1
C       DO IOFFSET = 0,1676,4
C         CALL SDFPKG(IPTRLOCATE)
C         CALL DUMPWORD(ILF)
C       ENDDO
C     ENDDO
C
C ACCESS THE DIRECTORY ROOT CELL
C
      CALL SDFPKG(IDIRLOCATE)
C     WRITE(IP,*) '*** DUMPING ROOT CELL'
C     DO II = 1,50
C       CALL DUMPWORD(ILF+II-1)
C     ENDDO
C
C ACCESS THE TITLE DATA CELL
C
      IPNTR = PGAF(ILF+23)
      CALL SDFPKG(IPTRLOCATE)
C     WRITE(IP,*) '*** SHOW TITLE INFO'
      DLINE = GETSTRING(ILB)
C     WRITE(IP,*) '*** AT D'
C     WRITE(IP,9000) DLINE
C
C FETCH THE SOURCE MEMBER NAME
C
      IDEX1 = 0
      IDEX2 = 0
      IX = LENTRIM(DLINE)
      DO II = 1,IX
        IF (DLINE(II:II) .EQ. '(') THEN
          IDEX1 = II
        ENDIF
        IF (DLINE(II:II) .EQ. ')') THEN
          IDEX2 = II
        ENDIF
      ENDDO
      IF ((IDEX1 .EQ. 0).OR.(IDEX2 .EQ. 0).OR.
     X    (IDEX1 .GT. IDEX2)) THEN
        WRITE(IP,*) '*** INCORRECT TITLE FORMAT: ',DLINE(1:IX)
        STOP 4100
      ENDIF
      CSOURCENAME = DLINE(IDEX1+1:IDEX2-1)
      WRITE(IP,*) 'CSOURCENAME = ',CSOURCENAME
C
C READ THE COMPRESSED SOURCE CORRESPONDING TO THIS SDF
C
C     WRITE(IP,*) '*** CALLING READMLST'
C     WRITE(IP,*) '*** AT E'
      CALL READMLST
C     WRITE(IP,*) '*** AT F'
C
C SHOW OUR COMPRESSED LINES
C
C     DO II = 1,NUMRECS
C       READ(IGL,REC=II) OUTBUF(1:133)
C       WRITE(IP,9000) OUTBUF(1:133)
C     ENDDO
C
C SHOW THE MOST-RECENTLY-READ SOURCE LINE
C
      IPREVSLOT = 0
C
C SHOW THE BLOCK NAMES AND THEIR SYMBOLS; COLLECT EQUATE EXTERNAL
C   NAMES SO WE CAN RESOLVE "A" REFERENCES THAT THE FAD BUILD PROGRAM
C   DEDUCES FROM THE 'EQUATE EXTERNALS'.  WE WILL ALSO CONSTRUCT
C   SETS OF PARALLEL ARRAYS THAT WILL ALLOW US TO INSTANTLY JUMP
C   FROM A STRUCTURE TERMINAL TO ITS TEMPLATE, AND THEN TO JUMP
C   FROM THE TEMPLATE TO ALL OF THE STRUCTURES THAT UTILIZE THAT
C   TEMPLATE.
C
      NUMEQUATES = 0
      NUMSTRUCS = 0
      NUMTERMS = 0
      NUMTPLS = 0
      DO IBLKNO = 1,NUMBLKS
C       WRITE(IP,*) 'IBLKNO = ',IBLKNO
        CALL SDFPKG(IBLKLOCATENUM)
C       WRITE(IP,*) 'BLKNAME = ',CBLKNAM
        DO ITHISSYMB = IFIRSTSYMB,ILASTSYMB
          ISYMBNO = ITHISSYMB
C         write(ip,*) 'call a'
          CALL SDFPKG(ISYMBLOCATENUM)
          ICLASS = ICHAR(PGAB(ILB+6))
          ITYPE = ICHAR(PGAB(ILB+7))
          IFLAGS = PGAF(ILF+2)
          INAMEFLAG = IAND(IFLAGS,2**26)
          IF (INAMEFLAG .GT. 0) THEN
            CNAME = 'NAME'
          ELSE
            CNAME = ' '
          ENDIF
C
C RECORD THE SYMBOL NUMBER OF THE EQUATE -- AND WHAT IT IS EQUATED TO.
C
          IF ((ICLASS .EQ. 2).AND.(ITYPE .EQ. 8)) THEN
            ISYMBTEMP = PGAH(ILH+9)
C
C GET THE ISN OF THE EQUATE SYMBOL'S DECLARE: WE CAN SAFELY
C  ASSUME THAT IT WILL BE THE FIRST XREF.
C
            ISTMTTEMP = 0
            IXREFINC = ICHAR(PGAB(ILB+3))
            IF (IXREFINC .GT. 0) THEN
              IXREFDEX = ILH + IXREFINC/2
              IXREFCNT = PGAH(IXREFDEX)
              IXREFDEX = IXREFDEX + 1
              ISTMTTEMP = PGAH(IXREFDEX)
            ENDIF
            IF ((ISYMBTEMP.GT.0).AND.(ISTMTTEMP.GT.0)) THEN
              NUMEQUATES = NUMEQUATES + 1
              IF (NUMEQUATES .GT. MAXEQUATES) THEN
                WRITE(IP,*) '*** MAXEQUATES EXCEEDED: ',MAXEQUATES
                STOP 4150
              ENDIF
              IEQUSYMB1(NUMEQUATES) = ISYMBNO
              IEQUSYMB2(NUMEQUATES) = ISYMBTEMP
              IEQUSTMT(NUMEQUATES) = ISTMTTEMP
              CSYMBNAMEQU(NUMEQUATES) = CSYMBNAM
C
C GET THE SRN/INCLUDE CNT FOR THIS EQUATE AND SAVE THE INFO
C
              ISTMTNO = ISTMTTEMP
              CALL SDFPKG(ISTMTNDXLOCATENUM)
              CSRNEQU(NUMEQUATES) = CSREFNO(1:6)
              INCLCNTEQU(NUMEQUATES) = INCLCNT
            ENDIF
          ENDIF
C         WRITE(IP,9570) ISYMBNO,CSYMBNAM,ICLASS,ITYPE,CNAME,
C    X      IFLAGS
 9570     FORMAT(1X,I4,' SYMB: ',A32,' CL#: ',I2,'  TP#: ',I2,
     X      2X,A4,2X,Z8)
C
C IF WE HAVE A TEMPLATE, SHOW ITS LAYOUT
C
          IBLANK = 0
          IF (CSYMBNAM(1:1) .EQ. ' ') IBLANK = 1
          IF ((ICLASS .EQ. 4).AND.(ITYPE .EQ. 16).AND.
     X        (IBLANK .EQ. 1)) THEN
C           WRITE(IP,9000) ' *** START TEMPLATE ***'
            ITPLSYMBNO = ISYMBNO
            ILEVEL = 0
            IDOSON = 0
            IDOBRO = 0
            IDOFATH = 0
            ISTOPSTRUC = 0
  141       CONTINUE
            ICLASS = ICHAR(PGAB(ILB+6))
            ITYPE = ICHAR(PGAB(ILB+7))
