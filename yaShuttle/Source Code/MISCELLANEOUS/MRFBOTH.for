          ISUBOK = 1
        ELSE IF ((ILITVAL .GT. 0).AND.(ILIT1 .GT. 0).AND.
     X    (ILIT2 .GT. 0)) THEN
          IF ((ILITVAL .GE. ILIT1).AND.
     X        (ILITVAL .LE. ILIT2)) THEN
C           WRITE(IP,*) '*** SUPER!  SUBSCRIPT WITHIN RANGE'
            ISUBOK = 1
          ENDIF
        ENDIF
        IF (ISUBOK .EQ. 0) THEN
          IX1 = LENTRIM(CSUBSCRIPT)
          IX2 = LENTRIM(CTEMP)
          IF (CSUBSCRIPT(IX1:IX1) .EQ. ':') THEN
