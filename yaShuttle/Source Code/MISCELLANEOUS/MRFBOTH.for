C     WRITE(IP,*) 'NUMSYMBS = ',NUMSYMBS
C     WRITE(IP,*) 'FIRSTISN = ',FIRSTISN
C     WRITE(IP,*) 'LASTISN = ',LASTISN
C     WRITE(IP,*) 'NUMSTMTS = ',NUMSTMTS
C     WRITE(IP,*) 'NUMEMITS = ',NUMEMITS
      GO TO 1000
C
C MODE 8 -- LOCATE BLOCK DATA CELL GIVEN BLOCK NUMBER
C
  8   CONTINUE
      CALL FBLKNUM
      GO TO 1000
C
C MODE 9 -- LOCATE SYMBOL DATA CELL GIVEN SYMBOL NUMBER
C
  9   CONTINUE
      CALL FSYMBNUM
      GO TO 1000
C
C MODE 10 -- LOCATE STATEMENT DATA CELL GIVEN ISN
C
 10   CONTINUE
      CALL FINDSTMT
      GO TO 1000
C
C MODE 11 -- FIND BLOCK DATA CELL GIVEN THE BLOCK NAME
C
 11   CONTINUE
      CALL FBLKNAME
      GO TO 1000
C
C MODE 12 -- LOCATE SYMBOL DATA CELL GIVEN BLOCK NAME AND SYMBOL NAME
C
 12   CONTINUE
C
C FIRST FIND THE BLOCK
C
      CALL FBLKNAME
C
C NOW FIND THE SYMBOL
C
      IF (ICRETURN .EQ. 0) THEN
        CALL FSYMBNAME
      ENDIF
      GO TO 1000
C
C MODE 13 -- LOCATE SYMBOL DATA CELL GIVEN SYMBOL NAME ONLY
C
 13   CONTINUE
      CALL FSYMBNAME
      GO TO 1000
C
C MODE 14 -- LOCATE STATEMENT DATA CELL GIVEN SRN
C
 14   CONTINUE
      CSAVE = CSREFNO
C
C DO A BINARY SEARCH
C
      ILOW = FIRSTISN
      IHIGH = LASTISN
 141  CONTINUE
      IF (IHIGH .LT. ILOW) GO TO 142
      ISTMTNO = (ILOW+IHIGH)/2
      CALL FINDSTMT
      IF (CSREFNO .EQ. CSAVE) THEN
        GO TO 1000
      ELSE IF (CSREFNO .LT. CSAVE) THEN
        ILOW = ISTMTNO + 1
        GO TO 141
      ELSE IF (CSREFNO .GT. CSAVE) THEN
        IHIGH = ISTMTNO - 1
        GO TO 141
      ENDIF
 142  CONTINUE
      ICRETURN = 1
      GO TO 1000
C
C MODE 15 -- LOCATE BLOCK INDEX TABLE ENTRY GIVEN BLOCK NUMBER
C
 15   CONTINUE
      CALL FBLKNDX
      GO TO 1000
C
C MODE 16 -- LOCATE SYMBOL INDEX TABLE ENTRY GIVEN SYMBOL NUMBER
C
 16   CONTINUE
      CALL FSYMBNDX
      GO TO 1000
C
C MODE 17 -- LOCATE STATEMENT INDEX TABLE ENTRY GIVEN STATEMENT NUMBER
C
 17   CONTINUE
      CALL FSTMTNDX
      GO TO 1000
C
C COMMON EXIT ROUTINE
C
 1000 CONTINUE
      RETURN
      END
C
C PTR-LOCATE SUBROUTINE
C
      SUBROUTINE PTRLOCATE
      INCLUDE (COMMBLK)
C
      IF ((IPAGE .LT. 0).OR.(IPAGE. GT. ILASTPAGE)) GO TO 100
      IF ((IOFFSET .LT. 0).OR.(IOFFSET .GT. 1680)) GO TO 100
      ILF = IPAGE*420 + IOFFSET/4 + 1
      ILH = IPAGE*840 + IOFFSET/2 + 1
      ILB = IPAGE*1680 + IOFFSET + 1
      RETURN
C
C ERROR ROUTINE
C
  100 CONTINUE
      WRITE(IP,9010) IPNTR
 9010 FORMAT(' PTR: ',Z8)
      WRITE(IP,*) 'IPAGE = ',IPAGE
      WRITE(IP,*) 'IOFFSET = ',IOFFSET
      WRITE(IP,*) 'ILASTPAGE = ',ILASTPAGE
      STOP 4005
      END
C
C FIND-BLOCK BY # SUBROUTINE
C
      SUBROUTINE FBLKNUM
      INCLUDE (COMMBLK)
      CHARACTER*255 GETSTRING
      CALL FBLKNDX
C     WRITE(IP,*) '*** CSECTNAM = ',CSECTNAM
C
C GO TO THE BLOCK DATA CELL
C
      IPNTR = PGAF(ILF+2)
      CALL PTRLOCATE
      IFIRSTSYMB = PGAH(ILH+16)
      ILASTSYMB = PGAH(ILH+17)
      CBLKNAM = GETSTRING(ILB+44)
C     WRITE(IP,*) '*** BLOCK NAME: ',CBLKNAM
C     WRITE(IP,*) '*** FIRST/LAST SYMBOL: ',IFIRSTSYMB,ILASTSYMB
      RETURN
      END
C
C FIND BLOCK INDEX ENTRY
C
      SUBROUTINE FBLKNDX
      INCLUDE (COMMBLK)
C
      IF ((IBLKNO .LT. 1).OR.(IBLKNO .GT. NUMBLKS)) STOP 4006
      IPNTR = IBLKNDXPTR
      CALL PTRLOCATE
      ILF = ILF + (IBLKNO-1)*3
      ILH = 2*(ILF-1) + 1
      ILB = 4*(ILF-1) + 1
      CSECTNAMINT(1) = PGAF(ILF)
      CSECTNAMINT(2) = PGAF(ILF+1)
      RETURN
      END
C
C SUBROUTINE TO FIND A BLOCK BY NAME
C
      SUBROUTINE FBLKNAME
      INCLUDE (COMMBLK)
      CHARACTER*255 CSAVE
      CSAVE = CBLKNAM
C     WRITE(IP,*) '*** NUMBLKS = ',NUMBLKS
      DO IBLKNO = 1,NUMBLKS
        CALL FBLKNUM
C       WRITE(IP,*) ' BLK#: ',IBLKNO,'  NAME: ',CBLKNAM
        IF (CBLKNAM .EQ. CSAVE) THEN
          RETURN
        ENDIF
      ENDDO
      ICRETURN = 2
      RETURN
      END
C
C FINDSYMBOL SUBROUTINE
C
      SUBROUTINE FSYMBNUM
      INCLUDE (COMMBLK)
C
      CALL FSYMBNDX
C
C GO TO THE SYMBOL DATA CELL
C
      IPNTR = PGAF(ILF+2)
      CALL PTRLOCATE
      ISYMBNLEN = ICHAR(PGAB(ILB+12))
C     WRITE(IP,*) 'ISYMBNLEN = ',ISYMBNLEN
      IF (ISYMBNLEN .GT. 8) THEN
        DO II = 1,(ISYMBNLEN-8)
          CSYMBNAM(II+8:II+8) = PGAB(ILB+23+II)
        ENDDO
      ENDIF
C     WRITE(IP,*) '*** SYMBOL NAME: ',CSYMBNAM
      RETURN
      END
C
C SUBROUTINE TO FIND A SYMBOL BY NAME
C
      SUBROUTINE FSYMBNAME
      INCLUDE (COMMBLK)
      CHARACTER*255 CSAVE
      CSAVE = CSYMBNAM
C
C LINEAR SEARCH (IT'S FAST ENOUGH AND GETS US TO THE FIRST
C   SYMBOL IN THE EVENT OF DUPLICATES).
C
      DO ISYMBNO = IFIRSTSYMB,ILASTSYMB
        CALL FSYMBNUM
        IF (CSYMBNAM .EQ. CSAVE) RETURN
      ENDDO
      ICRETURN = 1
      RETURN
      END
C
      SUBROUTINE FSYMBNDX
      INCLUDE (COMMBLK)
C
      IF ((ISYMBNO .LT. 1).OR.(ISYMBNO .GT. NUMSYMBS)) THEN
        WRITE(IP,*) 'ISYMBNO/NUMSYMBS = ',ISYMBNO,NUMSYMBS
        STOP 4007
      ENDIF
      IPNTR = ISYMBNDXPTR
      CALL PTRLOCATE
      ILF = ILF + (ISYMBNO-1)*3
      ILH = 2*(ILF-1) + 1
      ILB = 4*(ILF-1) + 1
      CSYMBNAM = ' '
      CSYMBNAMINT(1) = PGAF(ILF)
      CSYMBNAMINT(2) = PGAF(ILF+1)
      RETURN
      END
C
C FINDSTMT SUBROUTINE
C
      SUBROUTINE FINDSTMT
      INCLUDE (COMMBLK)
      CHARACTER*255 DLINE
C
      CALL FSTMTNDX
C
C GO TO THE STATEMENT DATA CELL
C
C     WRITE(IP,9511) 'INPTR1',PGAF(ILF+2)
C9511 FORMAT(A8,2X,Z8)
      IF (IDECLARE .EQ. 0) THEN
        IPNTR = PGAF(ILF+2)
C       WRITE(IP,9511) 'INPTR2',IPNTR
C       WRITE(IP,9512) IPAGE,IOFFSET
C9512   FORMAT('PAGE: ',Z4,'  OFFSET: ',Z4)
      ELSE IF (IDECLARE .EQ. 1) THEN
C
C INFORM THE CALLER THAT WE HAVE A DECLARE STATEMENT
C
        ITEMP = PGAF(ILF+2)
        IPNTR = -ITEMP
C       ITEMP = IPAGE
C       IPAGE = IOFFSET
C       IOFFSET = ITEMP
C       WRITE(IP,9511) 'INPTR4',IPNTR
C       WRITE(IP,9512) IPAGE,IOFFSET
      ENDIF
      CALL PTRLOCATE
      RETURN
      END
C
      SUBROUTINE FSTMTNDX
      INCLUDE (COMMBLK)
      CHARACTER*255 DLINE
C
      IF ((ISTMTNO .LT. FIRSTISN).OR.(ISTMTNO .GT. LASTISN)) THEN
        WRITE(IP,*) '*** ILLEGAL ISN: ',ISTMTNO
        STOP 4008
      ENDIF
      IDECLARE = 0
      IPNTR = ISTMTNDXPTR
      CALL PTRLOCATE
      ILF = ILF + (ISTMTNO-FIRSTISN)*3
      ILH = 2*(ILF-1) + 1
      ILB = 4*(ILF-1) + 1
      CSREFNO = ' '
      CSREFNOINT(1) = PGAF(ILF)
      CSREFNOINT(2) = PGAF(ILF+1)
      DLINE = CSREFNO(1:6)
      CSREFNO(1:6) = DLINE
      ITEMP = PGAF(ILF+2)
      IF (ITEMP .LT. 0) IDECLARE = 1
C     WRITE(IP,*) '*** SRN & INCLUDE CNT: ',CSREFNO(1:6),INCLCNT
      RETURN
      END
C
C DUMPWORD ROUTINE -- PRINT OUT 1 FULLWORD
C
      SUBROUTINE DUMPWORD(ILFX)
      INCLUDE (COMMBLK)
      CHARACTER CHAR4*4
      INTEGER*4 CHAR4INT
      EQUIVALENCE(CHAR4,CHAR4INT)
      CHAR4INT = PGAF(ILFX)
      IPAGEX = (ILFX-1)/420
      IOFFSETX = 4*((ILFX-1) - 420*IPAGEX)
      WRITE(IP,9005) IPAGEX,IOFFSETX,PGAF(ILFX),CHAR4
9005  FORMAT('PAGE: ',I6,' OFF: ',I4,2X,Z8,2X,A4)
      RETURN
      END
C
C THE GETSTRING ROUTINE
C
      CHARACTER*255 FUNCTION GETSTRING(ILBX)
      INCLUDE (COMMBLK)
      CHARACTER*128 CSTRING
      CSTRING = ' '
      ICOUNT = ICHAR(PGAB(ILBX))
      DO II = 1,ICOUNT
        CSTRING(II:II) = PGAB(ILBX+II)
      ENDDO
      GETSTRING = CSTRING
      RETURN
      END
C
      CHARACTER*255 FUNCTION MAKESRC(CINPUT,ILEN)
      INCLUDE (COMMBLK)
      CHARACTER*128 CSTRING
      CHARACTER*(*) CINPUT
      IX = LENTRIM(CINPUT)
      IOUT = 0
      CSTRING = ' '
      DO II = 1,IX
        IF (CINPUT(II:II) .NE. '_') THEN
          IOUT = IOUT + 1
          CSTRING(IOUT:IOUT) = CINPUT(II:II)
          IF (IOUT .GE. ILEN) GO TO 10
        ENDIF
      ENDDO
  10  CONTINUE
      MAKESRC = CSTRING
      RETURN
      END
C
C SUBROUTINE TO READ AND UNCOMPRESS COMPRESSED MASTER LISTING TAPE DATA
C
      SUBROUTINE READMLST
      INCLUDE (COMMBLK)
      INTEGER*4 JVAL
      CHARACTER*4 CVAL
      EQUIVALENCE (JVAL,CVAL)
      CHARACTER DLINE*250,CHAR1*1,CFULLNAME*64
C
      IOPEN = 999
      DO II = 1,3
        CFULLNAME = '/OOBS12.OI3017.MLSTPDS('
        IX = LENTRIM(CFULLNAME)
        IF (II .EQ. 1) THEN
          CFULLNAME(IX+1:64) = CSOURCENAME
        ELSE IF (II .EQ. 2) THEN
          CFULLNAME(IX+1:64) = CSOURCE2
        ELSE
          IUX = LENTRIM(CSOURCE2)
          CFULLNAME(IX+1:64) = CSOURCE2(1:IUX-1)
        ENDIF
        IX = LENTRIM(CFULLNAME)
        CFULLNAME(IX+1:IX+1) = ')'
C       WRITE(IP,*) 'CFULLNAME = ',CFULLNAME
        CLOSE(IMLST)
        OPEN(IMLST,FILE=CFULLNAME,STATUS='OLD',IOSTAT=IOPEN,
     X    ACCESS='SEQUENTIAL',FORM='UNFORMATTED',ACTION='READ')
        IF (IOPEN .EQ. 0) GO TO 107
      ENDDO
 107  CONTINUE
      IF (IOPEN .NE. 0) THEN
        WRITE(IP,*) '*** MLST OPEN ERROR: ',CFULLNAME
        STOP 4511
      ENDIF
C     WRITE(IP,*) 'FILE OPENED SUCCESSFULLY'
C
C OPEN A SCRATCH FILE TO KEEP OUR UNCOMPRESSED SOURCE LINES
C
      OPEN(IGL,STATUS='SCRATCH',RECL=133,
     X  IOSTAT=IOPEN,ACCESS='DIRECT',
     X  FORM='UNFORMATTED')
      IF (IOPEN .NE. 0) THEN
        WRITE(IP,*) '*** OPEN ERROR ON SCRATCH FILE FOR IGL'
        STOP 4002
      ENDIF
C     WRITE(IP,*) '*** SCRATCH FILE OPENED OK'
C
C INITIALIZE THE COMPRESSED LISTING BUFFER BEFORE WE DO OUR READ
C
      DO II = 1,MAXBYTESC
        INBUF(II) = CHAR(255)
      ENDDO
      INLOC = 0
      NUMRECS = 0
 5    CONTINUE
      READ(IMLST,ERR=100,END=100)
     X  (INBUF(II),II=INLOC+1,INLOC+133)
      INLOC = INLOC + 133
C     WRITE(IP,*) 'INLOC = ',INLOC
      IF ((INLOC+133) .GE. MAXBYTESC) STOP 4004
      GO TO 5
 100  CONTINUE
C
C NOW MOVE THROUGH THE INPUT BUFFER, DECOMPRESSING THE PRINT LINES INTO THE
C   OUTPUT BUFFER
C
      ILOCIN = 9
      ILOCOUT = 1
      ICHARSTRING = 0
      IBLNKREP = 0
      INBLNKREP = 0
      NUMEMITTED = 0
 10   CONTINUE
C     WRITE(IP,*) '*** AT 10'
      IF (ILOCOUT .GE. 134) THEN
        NUMRECS = NUMRECS + 1
        IF (NUMRECS .GT. MAXRECS) STOP 4010
C       WRITE(IP,*) '*** WRITING RECORD: ',NUMRECS
        WRITE(IGL,REC=NUMRECS) OUTBUF(1:133)
C       WRITE(IP,*) '*** FINISHED WRITE'
        CINDEX(NUMRECS) = OUTBUF(2:14)
C       WRITE(IP,9000) OUTBUF(1:133)
 9000   FORMAT(A)
        ILOCOUT = 1
      ENDIF
C
C GET THE NEXT CHARACTER
C
      CHAR1 = INBUF(ILOCIN)
      IVAL = ICHAR(CHAR1)
C     IF (ILOCIN .LE. 500) THEN
C       WRITE(IP,9040) ILOCIN,IVAL,ICHARSTRING,IBLNKREP,INBLNKREP,
C    X    ILOCOUT
C9040   FORMAT('*** AT LOC: ',I6,'  BYTE: ',Z2,'  FLAGS: ',3(2X,I1),
C    X    2X,I3)
C     ENDIF
C     DLINE = OUTBUF
C     DLINE(ILOCOUT:ILOCOUT) = '^'
C     WRITE(IP,9000) DLINE
C     WRITE(IP,9040) ILOCIN,IVAL,ICHARSTRING,IBLNKREP,INBLNKREP,
C    X  ILOCOUT
C     WRITE(IP,*) '*** AT A'
      IF (ICHARSTRING .EQ. 1) THEN
C *** GO TO CHARSTRING
        GO TO 20
      ENDIF
      IF (IVAL .EQ. 120) THEN
C *** SET UP BLANK REPETITION
        GO TO 30
      ENDIF
      IF (IVAL .EQ. 122) THEN
C *** SET UP NONBLANK REPETITION
        GO TO 40
      ENDIF
      IF (IVAL .EQ. 255) THEN
C *** TEST IF END-OF-DATA
        GO TO 50
      ENDIF
C
C SEE IF WE HAVE BLANK REPETITION
C
      IF (IBLNKREP .EQ. 1) GO TO 60
      IF (INBLNKREP .EQ. 1) GO TO 70
C
C TEST IF CHARACTER STRING COUNT
C
      IF (IVAL .GE. 128) THEN
C *** SET UP CHARACTER STRING
        GO TO 80
      ENDIF
C
C BLANKREP (IVAL CONTAINS THE # OF BLANKS - 1); MOVE IVAL+1 BLANKS TO THE
C  OUTPUT BUFFER AND INCREMENT ILOCOUT AS WE GO.
C
 60   CONTINUE
C     WRITE(IP,*) '*** AT BLANKREP: ',IVAL
      DO II = 1,IVAL+1
        OUTBUF(ILOCOUT:ILOCOUT) = ' '
        ILOCOUT = ILOCOUT + 1
      ENDDO
C
C CLEAR ALL FLAGS
C
      ICHARSTRING = 0
      IBLNKREP = 0
      INBLNKREP = 0
C
C ADVANCE THE INPUT POINTER TO THE NEXT BYTE
C
      ILOCIN = ILOCIN + 1
      GO TO 10
C CharString
 20   CONTINUE
C     WRITE(IP,*) '*** AT CHARSTRING: ',IPREVVAL
      DO II = 1,IPREVVAL+1
C       WRITE(IP,9100) INBUF(ILOCIN)
C9100   FORMAT('*** MOVING: ',Z2)
        OUTBUF(ILOCOUT:ILOCOUT) = INBUF(ILOCIN)
        ILOCIN = ILOCIN + 1
        ILOCOUT = ILOCOUT + 1
      ENDDO
      IBLANKREP = 1
      ICHARSTRING = 0
      INBLANKREP = 0
      GO TO 10
C SetupBlankRep
 30   CONTINUE
C     WRITE(IP,*) '*** AT SETUPBLANKREP'
      IBLNKREP = 1
      ICHARSTRING = 0
      INBLANKREP = 0
      ILOCIN = ILOCIN + 1
      GO TO 10
C SetupNonBlankRep
 40   CONTINUE
C     WRITE(IP,*) '*** AT SETUPNONBLANKREP'
      INBLNKREP = 1
      ICHARSTRING = 0
      IBLANKREP = 0
      ILOCIN = ILOCIN + 1
      GO TO 10
C EndOfData
 50   CONTINUE
C
C SHOW SOME OF THE INPUT BUFFER
C
C     DO II = 1,100
C       IVAL = INBUFF(II)
C       JVAL = IVAL
C       WRITE(IP,9050) II,IVAL,CVAL
C9050   FORMAT('INBUF:  ',I4,2X,Z8,2X,A4)
C     ENDDO
      RETURN
C NonBlankRep
 70   CONTINUE
C
C MOVE A STRING OF NON-BLANK CHARACTERS
C
C     WRITE(IP,*) 'NONBLANKREP: ',IVAL,ILOCIN
      DO II = 1,IVAL+1
        OUTBUF(ILOCOUT:ILOCOUT) = INBUF(ILOCIN+1)
        ILOCOUT = ILOCOUT + 1
      ENDDO
      ILOCIN = ILOCIN + 2
C
C CLEAR ALL FLAGS
C
      ICHARSTRING = 0
      IBLNKREP = 0
      INBLNKREP = 0
      GO TO 10
C SetupCharString
 80   CONTINUE
      ICHARSTRING = 1
      INBLNKREP = 0
      IBLANKREP = 0
      ILOCIN = ILOCIN + 1
      IPREVVAL = IVAL - 128
C     WRITE(IP,*) '*** AT SETUPCHARSTRING: ',IPREVVAL
      GO TO 10
      END
C
      INTEGER FUNCTION LENTRIM(INSTRING)
      CHARACTER INSTRING*(*)
      IX = LEN(INSTRING)
      IF (IX .EQ. 0) THEN
        LENTRIM = 0
        RETURN
      ENDIF
      DO II = IX,1,-1
        IF (INSTRING(II:II) .NE. ' ') THEN
          LENTRIM = II
          RETURN
        ENDIF
      ENDDO
      LENTRIM = 0
      RETURN
      END
