C           WRITE(IP,*) '*** OK, BUT: SUBSCRIPTS MATCH TO :'
C         ELSE
C           WRITE(IP,*) '*** DARN: ',CSUBSCRIPT(1:IX1),' <> ',
C    X        CTEMP(1:IX2)
C         ENDIF
        ENDIF
      ENDDO
      RETURN
      END
