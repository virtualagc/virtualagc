          ELSE
            CVARTHIS = '???'
          ENDIF
        ENDIF
        IWORD = PGAH(ILH1+1)
        IFLAG = IAND(RSHIFT(IWORD,15),1)
        NUMSYMENTS = IAND(IWORD,32767)
        NUMBYTESBODY = PGAH(ILH1) - 8 - 2*NUMSYMENTS
        NUMDESCRIPS = NUMBYTESBODY/2
        ILOC = ILH1 + 4 + NUMSYMENTS
