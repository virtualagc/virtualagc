          IF ((CMARK .EQ. CLASTCHAR).AND.(CMARK.NE.' ')) THEN
            CTEMP(IX:IX) = ','
          ENDIF
          IDISPL = 1
          IF ((CMARK .EQ. 'TO').OR.(CMARK .EQ. 'AT')) THEN
